export async function POST(request: Request) {
    try {
        const { messages } = await request.json();

        if (!messages || !Array.isArray(messages)) {
            return NextResponse.json(
                { error: 'Invalid or missing messages in request body' },
                { status: 400 }
            );
        }

        // Always include the initial instruction in the system message
        const systemPrompt = `${SYSTEM_MESSAGE}\n\n${INITIAL_INSTRUCTION}`;



        const stream = new ReadableStream({
            async start(controller) {
                try {
                    const response = await fetch(ANTHROPIC_API_URL, {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'X-API-Key': ANTHROPIC_API_KEY!,
                            'anthropic-version': '2023-06-01',
                        },
                        body: JSON.stringify({
                            messages: messages,
                            model: 'claude-3-5-sonnet-20240620',
                            max_tokens: 2096,
                            system: systemPrompt,  // Combined system message with instructions
                            stream: true,
                            temperature: 0.0,
                        }),
                    });

                    if (!response.ok) {
                        console.log(response);
                        throw new Error(`Anthropic API error: ${response.statusText}`);
                    }

                    const reader = response.body?.getReader();
                    if (!reader) {
                        throw new Error('Response body is null');
                    }
                    while (true) {
                        const { done, value } = await reader.read();
                        if (done) {
                            controller.close();
                            break;
                        }
                        controller.enqueue(value);
                    }
                } catch (error) {
                    controller.error(error);
                }
            },
        });

        return new Response(stream, {
            headers: {
                'Content-Type': 'text/event-stream',
                'Cache-Control': 'no-cache',
                'Connection': 'keep-alive',
            },
        });
    } catch (error) {
        console.error('Ошибка в API чата:', error);
        return NextResponse.json(
            {
                error: error instanceof Error
                    ? error.message
                    : 'Произошла неизвестная ошибка'
            },
            { status: 500 }
        );
    }
}
